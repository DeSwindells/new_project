radius = float(input("Enter the Radius of the Circle: "))

# Circumfrence = 2 * Pie * Radius
circumfrence = 2 * 3.14 * radius

# Area = Pie * Radius Squared
area = 3.14 * radius ** 2

print("Radius: ", radius)
print("Circumfrence: ", circumfrence)
print("Area: ", area)
